package Session11;

public class IfElse_Statements
{
    public static void main(String[] args)
    {
            int num=100;
            if(num>50 && num<70)
            {
                System.out.println("Number is between 50 and 70");
            }
            if(num>500 || num==500)
            {
            System.out.println("Number is either greater than 500 or equal to 500");
            }
            if(num<1000)
            {
            System.out.println("Number is greater than 100");
            }
            else
            {
                System.out.println("Number is smaller than 100");
            }


    }
}
